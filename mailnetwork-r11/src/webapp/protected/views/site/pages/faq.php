                <strong><a name="useful tips"> Who uses alternate addresses?</a></strong>
                <p>Mail-order companies, frequent travellers, foreign businesses, staff working abroad, home-based businesses, personal ad respondents, etc.</p>
                <strong><a name="mail privacy">How do I know my mail privacy will be secure?</a></strong>
                <p>Our Affiliated Centers are authorized CMRAs (Commercial Mail Receiving Agencies). The U.S. Post Office closely monitors mail-receiving agents to make sure they offer the same level of mail privacy as the U.S. Post Office itself. The fines and inevitable prison terms associated with mail tampering serve as a guarantee that your mail will be handled with the most care and professionalism. Experienced, bonded professionals handle your mail in a private and secure fashion.</p>
                <strong><a name="post office">Why shouldn't I simply rent a P.O. Box at the U.S. Post Office?</a></strong>
                <p>The U.S. Post Office requires that you apply in person for a P.O. Box, which is not very practical for out-of-state or out-of-country customers. Furthermore, P.O. Boxes only accept U.S. Mail, not FedEx and UPS packages, and mail cannot be forwarded to your actual home address from a P.O. Box.</p>
                <strong><a name="our records">Who has access to International Mail Forwarding Network records?</a></strong>
                <p>If you use the mailbox for business purposes, we can only give your name and real address to Law Enforcement Officials or Postal Inspectors in the event of a formal inquiry on their part. If you apply as an individual only, an actual court subpoena is required before we can give up information that concerns you.</p>
