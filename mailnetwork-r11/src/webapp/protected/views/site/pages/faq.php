                <strong><a name="my mail">Can I get my mail to my mailing address under several names?</a></strong>
                <p>When you use our mailing address, you can get your mail under three (3) different names (name or business name). You will need to provide the appropriate ID for each individual. To receive
                  mail under an additional name, you will need to pay additional fees.</p>
                <strong><a name="forwarding address">Can I change my forwarding address?</a></strong>
                <p>Yes. With International Mail Forwarding Network, it is possible to change your mail forwarding address. To do this, you must send your new forwarding address by e-mail at least 7 days
                  before you want the change of address to be made. A fee of $25 will be processed once the mail forwarding address has been changed.</p>
